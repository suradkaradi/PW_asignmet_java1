// Assignment Operator

// Assignment operator (=)
const myVariable = 20;
// Addition assignment operator (+=)
myVariable += 5; // myVariable is now equal to 25
// Subtraction assignment operator (-=)
myVariable -= 5; // myVariable is now equal to 20
// Multiplication assignment operator (*=)
myVariable *= 2; // myVariable is now equal to 40
// Division assignment operator (/=)
myVariable /= 2; // myVariable is now equal to 20
// Modulo assignment operator (%=)
myVariable %= 3; // myVariable is now equal to 1
