// Arthematic Operators in JS
// +[Addition] 
let num1 = 5
let num2 = 10
const result1 =  num1 + num2;
console.log(result1);

// -[Subtraction]

let num3 = 10
let num4 = 2
const result2 = num3 - num4;
console.log(result2);

// * [Multiplication]

let num5 = 10
let num6 = 20
const result3 = num5*num6;
console.log(result3);

// / [Division]

let num7 = 20
let num8 = 10
const result4 = num7/num8
console.log(result4)

// % [Modulus]

let num9 = 10
let num10 = 3
const result5 = num9 % num10;
console.log(result5)

// ** [Exponentiation]

let num11 = 3
let num12 = 2
const result6 = num11**num12
console.log(result6)